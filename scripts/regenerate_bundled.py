def _authored_node_pending(
    out_folder: Path, manifest_folder: Path,
    labels: tuple[str, ...], force: bool,
) -> list[str]:
    """Labels whose node corpus must be (re)generated for an authored manifold.

    The geometry (``manifold.json``: domain + per-node coords) is hand-maintained
    in the package tree — regen fills ``nodes/`` only, so the authored periodic /
    box / sphere layout survives a corpus regen.  ``--force`` regenerates every
    node (overwriting in place; the manifest is never touched); otherwise only
    the missing node files are pending.  The ``NN_<label>.json`` prefix matches
    the manifest's node order (``labels`` is that order).
    """
    if not (manifest_folder / "manifold.json").exists():
        raise SystemExit(
            f"authored manifold {manifest_folder.name!r}: no manifold.json in "
            f"the package tree to fill — author the geometry first"
        )
    if force:
        return list(labels)
    nodes = out_folder / "nodes"
    return [
        lab for i, lab in enumerate(labels)
        if not (nodes / f"{i:02d}_{lab}.json").exists()
    ]


def main() -> None:
    ap = argparse.ArgumentParser(
        description="Regenerate bundled manifolds under saklas/data/manifolds/.",
    )
    ap.add_argument(
        "targets", nargs="*",
        help=f"Manifold names to regenerate (default: all {len(SPECS)}).",
    )
    ap.add_argument(
        "--force", action="store_true",
        help="Wipe and regenerate even if a target already has node corpora.",
    )
    ap.add_argument(
        "--dry-run", action="store_true",
        help="Plan into a tempdir and print the roster; load no model, write "
             "nothing to the package tree.",
    )
    ap.add_argument(
        "--model", default=DEFAULT_MODEL_ID,
        help=f"HF model id for the generator (default {DEFAULT_MODEL_ID}).",
    )
    ap.add_argument(
        "--samples-per-prompt", dest="samples_per_prompt", type=int, default=1,
        help="In-character responses per baseline prompt, per node (default 1).",
    )
    ap.add_argument(
        "--neutral-samples", dest="neutral_samples", type=int, default=2,
        help="Responses per baseline prompt for the neutral baseline corpus "
             "(default 2 -> a denser covariance for the whitener / layer_means "
             "than the per-node default; decoupled from --samples-per-prompt).",
    )
    args = ap.parse_args()

    targets = args.targets or [s.name for s in SPECS] + [NEUTRAL_TARGET]
    unknown = [t for t in targets if t not in _SPEC_BY_NAME and t != NEUTRAL_TARGET]
    if unknown:
        raise SystemExit(
            f"unknown target(s): {', '.join(unknown)}\n"
            f"known: {', '.join([*_SPEC_BY_NAME, NEUTRAL_TARGET])}"
        )
    manifold_targets = [t for t in targets if t != NEUTRAL_TARGET]
    discover_targets = [
        t for t in manifold_targets if _SPEC_BY_NAME[t].fit_mode != "authored"
    ]
    authored_targets = [
        t for t in manifold_targets if _SPEC_BY_NAME[t].fit_mode == "authored"
    ]
    do_neutral = NEUTRAL_TARGET in targets

    from saklas.io.atomic import write_json_atomic
    from saklas.io.manifolds import (
        append_discover_manifold_node,
        plan_discover_generation,
    )

    out_root = MANIFOLDS_DIR
    tmp_out: tempfile.TemporaryDirectory[str] | None = None
    if args.dry_run:
        tmp_out = tempfile.TemporaryDirectory(prefix="saklas-bundled-dryrun-")
        out_root = Path(tmp_out.name)

    # Plan every discover target up front (no model) so a typo / spec error
    # fails fast and resume status is reported before the model loads.
    plans = {}
    for name in discover_targets:
        spec = _SPEC_BY_NAME[name]
        folder = out_root / name
        if args.force and folder.exists():
            shutil.rmtree(folder)
        plans[name] = plan_discover_generation(
            folder, name, spec.description,
            fit_mode=spec.fit_mode,
            labels=list(spec.labels),
            hyperparams=spec.hyperparams,
            node_kinds={label: spec.kind for label in spec.labels},
            tags=list(spec.tags) or None,
        )

    # Authored targets keep their hand-maintained manifest.json; regen only
    # (re)fills nodes/.  ``manifest_folder`` always reads the real package tree
    # (dry-run writes corpora into a tempdir but the geometry lives in the tree).
    authored_pending: dict[str, list[str]] = {}
    for name in authored_targets:
        spec = _SPEC_BY_NAME[name]
        authored_pending[name] = _authored_node_pending(
            out_root / name, MANIFOLDS_DIR / name, spec.labels, args.force,
        )

    # Neutral baseline is a pseudo-target: a single JSON corpus in the data
    # tree, not a manifold folder.  --force or a missing file marks it pending.
    neutral_pending = do_neutral and (args.force or not NEUTRAL_PATH.exists())

    if args.dry_run:
        print(f"[dry-run] planned {len(manifold_targets)} manifold(s) into a tempdir "
              f"(nothing written to {MANIFOLDS_DIR.relative_to(REPO)}):\n")
        for name in manifold_targets:
            spec = _SPEC_BY_NAME[name]
            extra = ("authored (manifest preserved)"
                     if spec.fit_mode == "authored" else f"hp={spec.hyperparams}")
            print(f"  {name:<26} {spec.kind:<8} {len(spec.labels):>3} nodes "
                  f"| {extra} | tags={list(spec.tags)}")
        gen_nodes = sum(len(_SPEC_BY_NAME[n].labels) for n in manifold_targets)
        summary = f"  total: {len(manifold_targets)} manifolds, {gen_nodes} nodes"
        if do_neutral:
            n_neutral = max(1, args.neutral_samples) * _baseline_prompt_count()
            state = "pending" if neutral_pending else "exists (--force to regen)"
            print(f"  {NEUTRAL_TARGET:<26} {'organic':<8} {n_neutral:>3} resp  "
                  f"| {NEUTRAL_PATH.relative_to(REPO)} [{state}]")
            summary += f" + neutral ({n_neutral} resp)"
        print(f"\n{summary}")
        if tmp_out is not None:
            tmp_out.cleanup()
        return

    for name in discover_targets:
        plan = plans[name]
        if not plan.pending:
            print(f"[{name}] already complete ({len(plan.index_of)} nodes)")
        elif plan.resumed and len(plan.pending) < len(plan.index_of):
            done = len(plan.index_of) - len(plan.pending)
            print(f"[{name}] resuming: {done}/{len(plan.index_of)} present, "
                  f"{len(plan.pending)} to generate")
    for name in authored_targets:
        spec, pend = _SPEC_BY_NAME[name], authored_pending[name]
        if not pend:
            print(f"[{name}] already complete ({len(spec.labels)} nodes, authored)")
        elif len(pend) < len(spec.labels):
            print(f"[{name}] resuming: {len(spec.labels) - len(pend)}/"
                  f"{len(spec.labels)} present, {len(pend)} to generate (authored)")
    if do_neutral and not neutral_pending:
        print(f"[{NEUTRAL_TARGET}] already present "
              f"({NEUTRAL_PATH.relative_to(REPO)}); --force to regen")

    need_model = [name for name in discover_targets if plans[name].pending]
    need_authored = [name for name in authored_targets if authored_pending[name]]
    if need_model or need_authored or neutral_pending:
        with tempfile.TemporaryDirectory(prefix="saklas-bundled-regen-") as tmp:
            os.environ["SAKLAS_HOME"] = tmp
            from saklas.core.session import SaklasSession

            print(f"loading generator model: {args.model}")
            session = SaklasSession.from_pretrained(
                args.model, device="auto", probes=[],
            )
            for name in need_model:
                spec, plan = _SPEC_BY_NAME[name], plans[name]
                folder = out_root / name
                poles = list(plan.pending)
                tot = len(plan.index_of)
                print(
                    f"\n[{name}] generating {len(poles)} node(s) as "
                    f"{spec.kind!r} ({args.samples_per_prompt} response(s) per "
                    f"baseline prompt each)..."
                )
                t0 = time.time()
                for label in poles:
                    corpora = session.generate_responses(
                        [label], [spec.kind],
                        custom_system=spec.system,
                        samples_per_prompt=args.samples_per_prompt,
                        on_progress=lambda msg, _n=name: print(
                            f"  [{_n}] {msg}", flush=True,
                        ),
                    )
                    append_discover_manifold_node(
                        folder, plan.index_of[label], label, corpora[label],
                    )
                    print(
                        f"  [{name}] [{plan.index_of[label] + 1}/{tot}] wrote "
                        f"node {label!r} ({len(corpora[label])} responses)",
                        flush=True,
                    )
                print(f"[{name}] generation finished in {time.time() - t0:.1f}s")

            # Authored manifolds: fill nodes/ only; manifold.json is preserved.
            for name in need_authored:
                spec = _SPEC_BY_NAME[name]
                folder = out_root / name
                pend = authored_pending[name]
                idx_of = {lab: i for i, lab in enumerate(spec.labels)}
                tot = len(spec.labels)
                print(
                    f"\n[{name}] generating {len(pend)} authored node(s) as "
                    f"{spec.kind!r} (manifest preserved; {args.samples_per_prompt} "
                    f"response(s) per baseline prompt each)..."
                )
                t0 = time.time()
                for label in pend:
                    corpora = session.generate_responses(
                        [label], [spec.kind],
                        custom_system=spec.system,
                        samples_per_prompt=args.samples_per_prompt,
                        on_progress=lambda msg, _n=name: print(
                            f"  [{_n}] {msg}", flush=True,
                        ),
                    )
                    append_discover_manifold_node(
                        folder, idx_of[label], label, corpora[label],
                    )
                    print(
                        f"  [{name}] [{idx_of[label] + 1}/{tot}] wrote "
                        f"node {label!r} ({len(corpora[label])} responses)",
                        flush=True,
                    )
                print(f"[{name}] generation finished in {time.time() - t0:.1f}s")

            if neutral_pending:
                print(
                    f"\n[{NEUTRAL_TARGET}] generating organic baseline "
                    f"({args.neutral_samples} response(s) per baseline prompt)..."
                )
                t0 = time.time()
                responses = session.generate_neutral_responses(
                    samples_per_prompt=args.neutral_samples,
                    on_progress=lambda msg: print(
                        f"  [{NEUTRAL_TARGET}] {msg}", flush=True,
                    ),
                )
                write_json_atomic(NEUTRAL_PATH, responses)
                print(
                    f"[{NEUTRAL_TARGET}] wrote {len(responses)} responses to "
                    f"{NEUTRAL_PATH.relative_to(REPO)} in {time.time() - t0:.1f}s"
                )

            del session
            if torch.cuda.is_available():
                torch.cuda.empty_cache()

    written = (
        [name for name in discover_targets if plans[name].pending]
        + [name for name in authored_targets if authored_pending[name]]
    )
    if written:
        print(f"\n[done] wrote {len(written)} manifold(s) -> fit with:")
        for name in written:
            print(f"  saklas manifold fit default/{name} -m <model>")
    if neutral_pending:
        print(f"\n[done] wrote neutral baseline -> {NEUTRAL_PATH.relative_to(REPO)}")
        print("  per-model layer_means + Mahalanobis whitener recompute on next "
              "session (neutral_statements.json hash changed)")


if __name__ == "__main__":
    main()
