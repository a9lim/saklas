def _baseline_prompt_count() -> int:
    """k for the bundled A2 baseline set (neutral corpus length is a multiple)."""
    with BASELINE_PROMPTS_PATH.open() as f:
        return len(json.load(f))

_CONCEPT_HP: dict[str, Any] = {"max_dim": 1, "var_threshold": 0.70}


@dataclass(frozen=True)
class ManifoldSpec:
    """Hardcoded generation parameters for one bundled manifold."""

    name: str
    kind: str                       # "abstract" | "concrete" | "custom" frame
    labels: tuple[str, ...]         # node roster (all generated by the model)
    hyperparams: dict[str, Any]
    description: str
    tags: tuple[str, ...] = ()
    fit_mode: str = "pca"
    # ``system`` is the custom elicitation system prompt for ``kind="custom"``
    # ({c} = the concept).  When ``fit_mode == "authored"`` the manifest
    # (domain + per-node coords) is *hand-maintained* in the package tree and
    # regen fills the node corpora only — preserving ``manifold.json`` — so an
    # authored periodic / box / sphere geometry survives a corpus regen.
    system: str | None = None


def _bipolar(name: str, tag: str, pos: str, neg: str, desc: str) -> ManifoldSpec:
    return ManifoldSpec(
        name=name, kind="abstract", labels=(pos, neg),
        hyperparams=dict(_CONCEPT_HP), description=desc, tags=(tag,),
    )


def _monopolar(name: str, tag: str, desc: str) -> ManifoldSpec:
    return ManifoldSpec(
        name=name, kind="abstract", labels=(name,),
        hyperparams=dict(_CONCEPT_HP), description=desc, tags=(tag,),
    )


